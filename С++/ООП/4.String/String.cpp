#include <string>
#include <iostream>

using namespace std;

int main( void) {
	setlocale(LC_ALL, "ru");

	int first_ind = 0;
	int last_ind = 0;

	cout << "Введите строку: ";
	string a;
	cin >> a;

	for (int i = 0; i < a.length(); i++) {
		if (a[i] == 'f') {
			first_ind = i;
			break;
		}
	}

	for (int i = a.length(); i > 0; i--) {
		if (a[i] == 'f') {
			last_ind = i;
			break;
		}
	}

	if (first_ind == last_ind)cout << "Индекс: " << first_ind;
	else cout<<"Индекс первого и последнего появления: " << first_ind << ',' << last_ind;



}

