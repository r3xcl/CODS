#include <string>
#include <iostream>

using namespace std;

int main()
{
	setlocale(LC_ALL, "ru");
	
	cout<<"Введите строку: ";
	string a;
	cin >> a;

	auto i1 = a.find ('h'); //поиск первого вхождения
	auto i2 = a.find ('h', i1 + 1); //поиск второго вхождения 

	a.erase(i1, i2 - i1 + 1); //удаление первого,второго вхождения и всех символов между ними

	cout << a << endl;

}

